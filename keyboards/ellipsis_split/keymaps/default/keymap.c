void keyboard_post_init_user(void) {
	layer_move(_WIN);  // Works without this but layer state is wrong for some reason
	if(is_keyboard_master()) {
		oled_init(OLED_ROTATION_270);
	}
}

void draw_raw(const char *data, uint16_t start) {
	for (uint16_t i = 0; i < GFX_SIZE; i++) {
        uint8_t c = pgm_read_byte(data++);
		oled_write_raw_byte(c, start + i);
    }
}

void draw_base(uint16_t start) {
	if(layer_state_is(_MAC)) {
		draw_raw(gfx_mac, start);
	}
	else {
		draw_raw(gfx_win, start);
	}
}

void draw_raise_lower(uint16_t start) {
	if(layer_state_is(_LOWER_MAC) || layer_state_is(_LOWER_WIN)) {
		draw_raw(gfx_lower, start);
	}
	else if(raise_mod) {
		draw_raw(gfx_raise, start);
	}
	else {
		draw_raw(gfx_no_value, start);
	}
}

void draw_thumbs(uint16_t start) {
	if(layer_state_is(_GAME)) {
		draw_raw(gfx_game, start);
	}
	else if(layer_state_is(_SPC)) {
		draw_raw(gfx_space, start);
	}
	else {
		draw_raw(gfx_no_value, start);
	}
}

void draw_locks(uint16_t start) {
	led_t led_state = host_keyboard_led_state();
	bool caps = led_state.caps_lock;
	bool num = layer_state_is(_NUM);
	const char* data = gfx_caps_num;

	if(!(caps || num)) {
		draw_raw(gfx_no_value, start);
		return;
	}
	for (uint16_t i = 0; i < GFX_SIZE; i++) {
        uint8_t c = pgm_read_byte(data++);
		// Caps indicator is on left half of image, num is on right
		bool left_half = (i % 32) < 16;
		if((caps && left_half) || (num && !left_half)) {
			oled_write_raw_byte(c, start + i);
		}
		else {
			oled_write_raw_byte(0x00, start + i);
		}
    }
}

void oled_task_user(void) {
	if(is_keyboard_master()) {
		draw_base(0);
		draw_raise_lower(GFX_SIZE);
		draw_thumbs(2* GFX_SIZE);
		draw_locks(3* GFX_SIZE);
	}
}

void matrix_scan_user(void) {
	if(is_keyboard_master()) {
		oled_task();
	}
}

bool process_cycle_space(uint16_t keycode, keyrecord_t *record) {
	if(keycode == KC_CYCLE_SPACE && record->event.pressed) {
		if(layer_state_is(_SPC)) {
			layer_off(_SPC);
		}
		else if(layer_state_is(_GAME)) {
			layer_off(_GAME);
			layer_on(_SPC);
		}
		else {
			layer_on(_GAME);
		}
		return false;
	}
	return true;
}

bool process_raise(uint16_t kc, keyrecord_t *record) {
	if(kc == KC_RAISE) {
		if(record->event.pressed) {
			raise_mod = layer_state_is(_MAC) ? MOD_LGUI : MOD_LCTL;
			register_mods(raise_mod);
		}
		else {
			unregister_mods(raise_mod);
			raise_mod = 0;
		}
		return false;
	}

	if (raise_mod) {
		uint8_t new_mod = raise_mod;

		if(layer_state_is(_MAC)) {
			if (kc == KC_SPC ||
				kc == KC_BSPC ||
				kc == KC_LEFT ||
				kc == KC_RIGHT ||
				kc == KC_UP ||
				kc == KC_DOWN) {
				new_mod = MOD_LALT;
			}
			else {
				new_mod = MOD_LGUI;
			}
		}
		else {
			if (kc == KC_Q) {
				tap_code16(LALT(KC_F4));
				return false;
			}
			else if (kc == KC_TAB || kc == KC_SPC) {
				new_mod = MOD_LALT;
			}
			else {
				new_mod = MOD_LCTL;
			}
		}

		if (new_mod != raise_mod) {
			unregister_mods(raise_mod);
			register_mods(new_mod);
			raise_mod = new_mod;
		}
	}

	return true;
}

bool process_num(uint16_t keycode, keyrecord_t *record) {
	if(keycode == KC_NUM && record->event.pressed) {
		tap_code(KC_NUMLOCK);
		layer_invert(_NUM);
		return false;
	}

	return true;
}

bool process_record_user(uint16_t keycode, keyrecord_t *record) {
#ifdef CONSOLE_ENABLE
	// If console is enabled, it will print the matrix position and status of each key pressed
    uprintf("KL: kc: %u, col: %u, row: %u, pressed: %u\n", keycode, record->event.key.col, record->event.key.row, record->event.pressed);
#endif 

	if(!process_raise(keycode, record)) { return false; }
	if(!process_cycle_space(keycode, record)) { return false; }
	if(!process_num(keycode, record)) { return false; }

  	return true;
}